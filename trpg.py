"""
trpg.py
===========

이 모듈은 Discord 봇을 위한 자유도 기반 TRPG 시스템을 구현합니다.  
캐릭터 생성, 모험 시작, 자유 행동 판정 기능을 포함하며, 모든 캐릭터 정보와 진행 상태는 Supabase에 저장됩니다.  
주사위 굴림과 스탯 기반 판정은 시스템 규칙을 우선 적용하고, AI(Gemini)는 상황 서술만 담당합니다.  

명령어 구조
------------

모든 TRPG 관련 슬래시 명령어는 `/trpg` 하위 명령어 형태로 제공됩니다.

- `/trpg 캐릭터생성` – 캐릭터를 생성합니다. 스탯 합계가 100을 넘지 않도록 제한합니다.  
- `/trpg 모험시작` – AI 게임마스터(GM)가 현재 상황을 설명하며 모험을 시작합니다.  
- `/trpg 행동 (내용)` – 자유롭게 행동을 입력하여 판정과 결과를 진행합니다.  

Supabase 테이블 구조 (SQL 예시)
-----------------------------

```sql
CREATE TABLE trpg_characters (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id TEXT NOT NULL,
    guild_id TEXT NOT NULL,
    name TEXT,
    gender TEXT,
    job TEXT,
    stats JSONB,
    hp INTEGER,
    inventory JSONB,
    current_state TEXT,
    created_at TIMESTAMPTZ DEFAULT timezone('utc', now()),
    CONSTRAINT trpg_characters_user_guild_unique UNIQUE (user_id, guild_id)
);
```

각 레코드는 사용자와 길드에 대해 하나의 캐릭터를 나타냅니다. `current_state` 컬럼은 진행 중인 모험의 상황 설명을 저장하는 데 사용합니다.
"""

