        try:
            self.bot.tree.add_command(self.trpg_group)
            print("✅ TRPG 명령어 그룹 등록 완료!")
        except Exception as e:
            print(f"⚠️ TRPG 명령어 그룹 등록 실패 또는 이미 등록됨: {e}")


